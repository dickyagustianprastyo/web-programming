  function updateHourOptions() {
    const jenisSelect = document.getElementsByName('jenis')[0];
    const hourSelect = document.getElementsByName('pilih')[0];
    hourSelect.innerHTML = ''; // Menghapus semua opsi saat jenis berubah

    if (jenisSelect.value === 'Traditional_sandwich_generation') {
      const tradisionalOptions = [
        'Traditional Sandwich Inggris',
        'Traditional Sandwich Italy',
        'Traditional Sandwich Turky'
      ];

      tradisionalOptions.forEach(option => {
        const optionElement = document.createElement('option');
        optionElement.value = option.replace(/\s+/g, '_');
        optionElement.textContent = option;
        hourSelect.appendChild(optionElement);
      });
    } else if (jenisSelect.value === 'Club_Sandwich_Generation') {
      const clubOptions = [
        'Club Sandwich Belanda',
        'Club Sandwich Belgia',
        'Club Sandwich Meksiko'
      ];

      clubOptions.forEach(option => {
        const optionElement = document.createElement('option');
        optionElement.value = option.replace(/\s+/g, '_');
        optionElement.textContent = option;
        hourSelect.appendChild(optionElement);
      });
    } else if (jenisSelect.value === 'Open_faced_sandwich_generation') {
      const openFacedOptions = [
        'Open faced sandwich Jerman',
        'Open faced sandwich Prancis',
        'Open faced sandwich Spanyol'
      ];

      openFacedOptions.forEach(option => {
        const optionElement = document.createElement('option');
        optionElement.value = option.replace(/\s+/g, '_');
        optionElement.textContent = option;
        hourSelect.appendChild(optionElement);
      });
    }
  }

  const jenisSelect = document.getElementsByName('jenis')[0];
  jenisSelect.addEventListener('change', updateHourOptions);

  const form = document.getElementById('form-submit');
  form.addEventListener('submit', function(event) {
    event.preventDefault(); // Mencegah pengiriman form secara default

    const jenisSelect = document.getElementsByName('jenis')[0];
    const hourSelect = document.getElementsByName('pilih')[0];
    const nameInput = document.getElementById('name');
    const seatInput = document.getElementById('no_meja_kosong');
    const personSelect = document.querySelector('.person');

    const selectedJenis = jenisSelect.options[jenisSelect.selectedIndex].text;
    const selectedHour = hourSelect.options[hourSelect.selectedIndex].text;
    const name = nameInput.value;
    const seatNumber = seatInput.value;
    const numberOfPersons = personSelect.options[personSelect.selectedIndex].text.split('-')[0].trim();

    const orderDetails = `
      <h3>Pesanan Anda:</h3>
      <p>Atas Nama: ${name}</p>
      <p>Nomor Kursi: ${seatNumber}</p>
      <p>Jumlah Pesanan: ${numberOfPersons}</p>
      <p>kategori Sandwich: ${selectedJenis}</p>
      <p>Jenis Sandwich: ${selectedHour}</p>
      <p>Lama Pembuatan: ${getRandomTime()} Menit/Detik</p>
    `;

    const orderSummary = document.createElement('div');
    orderSummary.innerHTML = orderDetails;

    const bookTableSection = document.getElementById('book-table');
    bookTableSection.innerHTML = ''; // Menghapus form setelah pesanan dikirim
    bookTableSection.appendChild(orderSummary);
  });
  function getRandomTime() {
  const hours = Math.floor(Math.random() * 24);
  const minutes = Math.floor(Math.random() * 60);
  const formattedHours = hours.toString().padStart(2, '0');
  const formattedMinutes = minutes.toString().padStart(2, '0');

  const deliveryHours = Math.floor(minutes / 60); // Calculate delivery time in hours
  const deliveryMinutes = minutes % 60; // Calculate remaining minutes for delivery

  const formattedDeliveryHours = deliveryHours.toString().padStart(2, '0');
  const formattedDeliveryMinutes = deliveryMinutes.toString().padStart(2, '0');

  return `${formattedHours}:${formattedMinutes} (Estimasi pengantaran: ${formattedDeliveryHours}:${formattedDeliveryMinutes})`;
}



